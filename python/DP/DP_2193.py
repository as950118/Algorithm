dp = [0]*91
dp[0] = 0
dp[1] = 1

def func(n, i):
    if n<i:
        return 0
    dp[i] += dp[i-1] + dp[i-2]
    func(n, i+1)


N = int(input())
func(N, 2)
print(dp[N])
